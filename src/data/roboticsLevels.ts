// 100 Robotics Navigation Levels with Progressive Difficulty
// Algorithms: A*, Dijkstra, BFS

export interface RoboticsLevel {
  id: number;
  name: string;
  difficulty: 'Dễ' | 'Trung bình' | 'Khó' | 'Chuyên gia';
  algorithm: 'A*' | 'Dijkstra' | 'BFS' | 'Mixed';
  grid: number[][];
  start: [number, number];
  end: [number, number];
  obstacles?: [number, number][];
  movingObstacles?: Array<{
    position: [number, number];
    pattern: 'circular' | 'horizontal' | 'vertical' | 'random';
    speed: number;
  }>;
  timeLimit: number; // seconds
  minMoves: number;
  description: string;
  tips: string[];
  pointValue: number;
}

// Helper function to generate grid
const createGrid = (
  width: number,
  height: number,
  walls: [number, number][] = [],
): number[][] => {
  const grid = Array(height)
    .fill(null)
    .map(() => Array(width).fill(0));
  walls.forEach(([x, y]) => {
    if (x >= 0 && x < height && y >= 0 && y < width) {
      grid[x][y] = 1;
    }
  });
  return grid;
};

